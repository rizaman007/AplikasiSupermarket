    private void tampil() {
        try{
            ArrayList data = trans.tampil();
            for(int i = 0;i < data.size()-1;i+=4)
            {
                String nomor = (String)data.get(i);
                String tanggal = (String)data.get(i+1);
                int harga = (int)data.get(i+2);
                String kode = (String)data.get(i+3);

                String[] data_field = {nomor.trim(),tanggal.trim(),String.valueOf(harga),kode.trim()};
                model = (DefaultTableModel)tb_transaksi.getModel();
                model.addRow(data_field);
            }
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan" + ex.getMessage());
            ex.printStackTrace();
        }
    }
    
    private String cari() {
        int baris = tb_transaksi.getSelectedRow();
        model = (DefaultTableModel) tb_transaksi.getModel();
        String id = model.getValueAt(baris,0).toString();
        return id;
    }

