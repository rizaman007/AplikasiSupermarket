    private void tampil() {
        try{
            ArrayList data = akun.tampil();
            for(int i = 0;i < data.size()-1;i+=4)
            {
                String username = (String)data.get(i);
                String level = (String)data.get(i+2);
                String kode = (String)data.get(i+3);

                String[] data_field = {username.trim(),level.trim(),kode.trim()};
                model = (DefaultTableModel)tb_akun.getModel();
                model.addRow(data_field);
            }
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan" + ex.getMessage());
            ex.printStackTrace();
        }
    }
    private String cari() {
        int baris = tb_akun.getSelectedRow();
        model = (DefaultTableModel) tb_akun.getModel();
        String id = model.getValueAt(baris,0).toString();
        return id;
    }

